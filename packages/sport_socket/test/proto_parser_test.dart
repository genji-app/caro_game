import 'dart:typed_data';

import 'package:sport_socket/src/parser/proto_parser.dart';
import 'package:sport_socket/src/proto/proto.dart';
import 'package:test/test.dart';

void main() {
  group('ProtoParser', () {
    late ProtoParser parser;

    setUp(() {
      parser = ProtoParser();
    });

    test('parse returns null for empty bytes', () {
      final result = parser.parse(Uint8List(0));
      expect(result, isNull);
    });

    test('parse returns null for malformed data', () {
      final result = parser.parse(Uint8List.fromList([0xFF, 0xFF, 0xFF]));
      expect(result, isNull);
    });

    test('parseWithDetails returns error for empty bytes', () {
      final result = parser.parseWithDetails(Uint8List(0));
      expect(result.isError, isTrue);
      expect(result.error, 'Empty bytes received');
    });

    test('parse decodes sample EventResponse message correctly', () {
      // Sample binary message from real WebSocket data
      // Channel: ln:vi:s:1:tr:0:e
      // Type: u (update)
      // Data: EventResponse
      final sampleBytes = Uint8List.fromList(_sampleEventResponseBytes);

      final result = parser.parse(sampleBytes);

      expect(result, isNotNull);
      expect(result!.channel, equals('ln:vi:s:1:tr:0:e'));
      expect(result.type, equals('u'));
      expect(result.whichData(), equals(Payload_Data.event));
      expect(result.hasEvent(), isTrue);

      final event = result.event;
      expect(event.sportId, equals(1));
      expect(event.leagueId, equals(1485)); // 0xCD 0x0B in varint
      expect(event.homeName, equals('Italy (V)'));
      expect(event.awayName, equals('Uruguay (V)'));
      expect(event.isLive, isTrue);
      expect(event.markets.isNotEmpty, isTrue);
    });

    test('parse decodes market and odds data correctly', () {
      final sampleBytes = Uint8List.fromList(_sampleEventResponseBytes);
      final result = parser.parse(sampleBytes);

      expect(result, isNotNull);
      final event = result!.event;

      // Check markets
      expect(event.markets.length, greaterThan(0));

      final firstMarket = event.markets.first;
      expect(firstMarket.oddsList.isNotEmpty, isTrue);

      // Check odds
      final firstOdds = firstMarket.oddsList.first;
      expect(firstOdds.oddsHome.decimal, isNotEmpty);
    });

    test('Payload_Data enum identifies data type correctly', () {
      final sampleBytes = Uint8List.fromList(_sampleEventResponseBytes);
      final result = parser.parse(sampleBytes);

      expect(result, isNotNull);

      // Test whichData() for routing
      switch (result!.whichData()) {
        case Payload_Data.event:
          expect(result.hasEvent(), isTrue);
          break;
        case Payload_Data.league:
          fail('Expected event, got league');
        case Payload_Data.hotEvent:
          fail('Expected event, got hotEvent');
        case Payload_Data.outright:
          fail('Expected event, got outright');
        case Payload_Data.betSlipStatus:
          fail('Expected event, got betSlipStatus');
        case Payload_Data.notSet:
          fail('Expected event, got notSet');
      }
    });
  });
}

