  /// Danh sách tất cả URL remote ([REMOTE_PATH]) để preload.
  static List<String> get remoteUrlsForPreload =>
      _remoteIconSuffixes.map((s) => '$REMOTE_PATH/$s').toList();

  /// Icon dùng trên màn hình home — preload ưu tiên trước (welcome, bottom nav, sections).
  static List<String> get remoteUrlsForPreloadHomeFirst =>
      _remoteIconSuffixesHomeFirst.map((s) => '$REMOTE_PATH/$s').toList();

  /// Danh sách URL icon preload sắp theo từng màn hình: home → sport → sport_detail → bet_detail → casino → parlay → auth → profile → còn lại.
  static List<String> get remoteUrlsForPreloadOrderedByScreen {
    final ordered = _orderedSuffixesByScreen();
    final all = _remoteIconSuffixes.toSet();
    final seen = ordered.toSet();
    final rest = all.difference(seen).toList();
    return [...ordered, ...rest].map((s) => '$REMOTE_PATH/$s').toList();
  }

  /// Giống remoteUrlsForPreloadOrderedByScreen nhưng loại trừ icon game (preload phase 1).
  static List<String> get remoteUrlsForPreloadOrderedByScreenExcludeGame {
    return remoteUrlsForPreloadOrderedByScreen;
  }

  /// Chỉ icon game (preload phase 2, sau khi xong phase 1).
  static List<String> get remoteUrlsForPreloadGameOnly =>
      _remoteIconSuffixesGame.map((s) => '$REMOTE_PATH/$s').toList();

  static List<String> _orderedSuffixesByScreen() {
    const screens = [
      _remoteIconSuffixesHomeFirst,
      _remoteIconSuffixesSport,
      _remoteIconSuffixesSportDetail,
      _remoteIconSuffixesBetDetail,
      _remoteIconSuffixesCasino,
      // _remoteIconSuffixesGame,
      _remoteIconSuffixesParlay,
      _remoteIconSuffixesAuth,
      _remoteIconSuffixesProfile,
    ];
    final result = <String>[];
    final seen = <String>{};
    for (final list in screens) {
      for (final s in list) {
        if (seen.add(s)) result.add(s);
      }
    }
    return result;
  }

